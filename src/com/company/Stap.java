package com.company;

public class Stap {
    private String PuntA;
    private String PuntB;

    public Stap(String puntA, String puntB) {
        this. PuntA = puntA;
        this. PuntB = puntB;
    }

    public String getPuntA() {
        return PuntA;
    }

    public void setPuntA(String puntA) {
        PuntA = puntA;
    }

    public String getPuntB() {
        return PuntB;
    }

    public void setPuntB(String puntB) {
        PuntB = puntB;
    }
}


